/*
====REGRAS====
1. Toda celula viva com menos de dois vizinhos vivos morre de solidão
2. Toda célula com dois ou três vizinhos vivos mantem o estado atual
3. Toda célula com mais de três vizinhos vivos morre devido à superpopulação.
4. Toda célula morta com exatamente 3 vizinhos vivos se torna viva(na proxima gerãção?).
*/
function Setup(){
	window.max_grid = 30;
	max_grid = window.max_grid;
	window.grid = new Array(max_grid);
	for(var i=0; i <max_grid;i++) {
		window.grid[i] = new Array(max_grid);
		for(var j=0; j <max_grid;j++) {
			window.grid[i][j] = 0;
		}
	}
	window.gridWork = new Array(max_grid);
	for(var i=0; i <max_grid;i++) {
		window.gridWork[i] = new Array(max_grid);
		for(var j=0; j <max_grid;j++) {
			window.gridWork[i][j] = 0;
		}
	}

	window.vizinhos = new Array(max_grid);
	for(var i=0; i <max_grid;i++) {
		window.vizinhos[i] = new Array(max_grid);
		for(var j=0; j <max_grid;j++) {
			window.vizinhos[i][j] = 0;
		}
	}

}

function AplicaRegras(){
	max = window.max_grid;
	VerificaVizinhos();
	for (var i = 0; i < max; i++){
		for (var j = 0; j < max; j++){
			//vizinhos = window.vizinhos[i][j];
			if(window.vizinhos[i][j] < 2 || window.vizinhos[i][j] > 3){
				window.gridWork[i][j] = 0;
			}
			else if(window.vizinhos[i][j] == 3){
				window.gridWork[i][j] = 1;
			}
			else if(window.vizinhos[i][j] == 2){
				window.gridWork[i][j] = window.grid[i][j];
			}
			else {
				return "WTF!";
			}
		}
	}
	window.grid = window.gridWork;
	
}
function VerificaVizinhos(){
	//recebe as coordenadas da celula e retorna a quantidade de vizinhos vivos
	var vizinhosVivos = 0;
	for(var x = 0; x<window.max_grid ; x ++){
		for(var y = 0; y<window.max_grid ; y ++){
			if(y-1 >= 0){ //verifica celulas a esquerda
				if(x-1 >=0){
					if(window.grid[x-1][y-1] == 1){
						vizinhosVivos += 1;
					}
				}
				if(x+1 < window.max_grid){
					if(window.grid[x+1][y-1] == 1){
						vizinhosVivos += 1;
					}
				}
				if(window.grid[x][y-1] == 1){
					vizinhosVivos += 1;
				}

			}

			if(y+1 < window.max_grid){//verifica celulas a direita
				if(x-1 >=0){
					if(window.grid[x-1][y+1] == 1){
						vizinhosVivos += 1;
					}
				}
				if(x+1 < window.max_grid){
					if(window.grid[x+1][y+1] == 1){
						vizinhosVivos += 1;
					}
				}
				if(window.grid[x][y+1] == 1){
					vizinhosVivos += 1;
				}

			}

			if(x-1 >= 0 && window.grid[x-1][y] == 1){ //verifica celula acima
				vizinhosVivos += 1;
			}
			if(x+1 < window.max_grid && window.grid[x+1][y] == 1){//verifica celula abaixo
				vizinhosVivos += 1;
			}
			window.vizinhos[x][y] = vizinhosVivos;
			vizinhosVivos = 0;
		}
	}
}

function ImprimeGrid(){//funcao para auxiliar no DEV
	texto = new String("GRID<BR>");
	for (var i = 0; i < window.max_grid; i++){
		for (var j = 0; j < window.max_grid; j++){
			texto += window.grid[i][j];
			texto += ", ";
		}
		texto += "<br>";
	}
	document.getElementById("texto").innerHTML = texto;
}

function ImprimeGridWork(){//funcao para auxiliar no DEV
	texto = new String("GRID WORK<BR>");
	for (var i = 0; i < window.max_grid; i++){
		for (var j = 0; j < window.max_grid; j++){
			texto += window.gridWork[i][j];
			texto += ", ";
		}
		texto += "<br>";
	}
	document.getElementById("texto").innerHTML = texto;
}

