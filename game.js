function draw(){
	var canvasElem = document.getElementById('canvas');
	var celulaLado = 15;
	if(canvasElem.getContext) {
		var ctx = canvasElem.getContext('2d');
		ctx.fillStyle = "rgb(220,220,255)";
		ctx.fillRect(0, 0, 500, 500);
		for (var i = 0; i<window.max_grid; i ++){
			for (var j = 0; j<window.max_grid; j ++){
				var x = 10+(i*16);
				var y = 10+(j*16);
				if(window.grid[i][j] == 0){
					ctx.fillStyle = "rgb(80, 80, 100)";
					ctx.fillRect(x, y, celulaLado, celulaLado);
				}
				else {
					ctx.fillStyle = "rgb(200,190,0)";
					ctx.fillRect(x, y, celulaLado, celulaLado);
				}
			}
		}
	}	
}

